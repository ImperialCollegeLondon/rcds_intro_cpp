/*
  Exercise 1: Variables
*/

#include <iostream>
using namespace std;

int main() {

  // Declare variable of type integer
  int x;
  x = 4;

  // Print on screen
  cout << "The value of x is " << x << endl;

  // Declare variables of type double
  double y;
  double z;
  y = 3.141;
  z = 1.618;

  // Print on screen
  cout << "The value of y is " << y << " and the value of z is " << z << endl;

  return 0;

}